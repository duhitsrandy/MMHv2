#!/usr/bin/env node
/**
 * Creates MeetMeHalfwayMobile/.env from repo .env.local (gitignored).
 * Does not print secret values.
 */
const fs = require("node:fs");
const path = require("node:path");

const repoRoot = process.cwd();
const rootEnvPath = path.join(repoRoot, ".env.local");
const mobileEnvPath = path.join(repoRoot, "MeetMeHalfwayMobile", ".env");
const examplePath = path.join(repoRoot, "MeetMeHalfwayMobile", ".env.example");

function parseEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return {};
  const lines = fs.readFileSync(filePath, "utf8").split(/\r?\n/);
  const entries = {};
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const idx = trimmed.indexOf("=");
    if (idx <= 0) continue;
    const key = trimmed.slice(0, idx).trim();
    let value = trimmed.slice(idx + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    entries[key] = value;
  }
  return entries;
}

const root = parseEnvFile(rootEnvPath);
if (!Object.keys(root).length) {
  console.error("[sync-mobile-env] Missing or empty .env.local at repo root.");
  console.error("  Copy .env.example to .env.local and fill in keys first.");
  process.exit(1);
}

const pick = (keys) => {
  for (const key of keys) {
    const v = root[key];
    if (v) return v;
  }
  return "";
};

const apiBase =
  root.EXPO_PUBLIC_API_BASE_URL ||
  root.MOBILE_API_BASE_URL ||
  "http://localhost:3000";

const lines = [
  "# Generated by npm run mobile:sync-env — do not commit",
  `EXPO_PUBLIC_API_BASE_URL=${apiBase}`,
  `EXPO_PUBLIC_CLERK_PUBLISHABLE_KEY=${pick([
    "EXPO_PUBLIC_CLERK_PUBLISHABLE_KEY",
    "NEXT_PUBLIC_CLERK_PUBLISHABLE_KEY",
  ])}`,
  `EXPO_PUBLIC_SUPABASE_URL=${pick([
    "EXPO_PUBLIC_SUPABASE_URL",
    "NEXT_PUBLIC_SUPABASE_URL",
  ])}`,
  `EXPO_PUBLIC_SUPABASE_ANON_KEY=${pick([
    "EXPO_PUBLIC_SUPABASE_ANON_KEY",
    "NEXT_PUBLIC_SUPABASE_ANON_KEY",
  ])}`,
  `EXPO_PUBLIC_STRIPE_PUBLISHABLE_KEY=${pick([
    "EXPO_PUBLIC_STRIPE_PUBLISHABLE_KEY",
    "NEXT_PUBLIC_STRIPE_PUBLISHABLE_KEY",
  ])}`,
  `EXPO_PUBLIC_STRIPE_PRICE_PLUS_MONTHLY=${pick([
    "EXPO_PUBLIC_STRIPE_PRICE_PLUS_MONTHLY",
    "NEXT_PUBLIC_STRIPE_PRICE_PLUS_MONTHLY",
  ])}`,
  `EXPO_PUBLIC_STRIPE_PRICE_PRO_MONTHLY=${pick([
    "EXPO_PUBLIC_STRIPE_PRICE_PRO_MONTHLY",
    "NEXT_PUBLIC_STRIPE_PRICE_PRO_MONTHLY",
  ])}`,
  `EXPO_PUBLIC_STRIPE_PRICE_BUSINESS_MONTHLY=${pick([
    "EXPO_PUBLIC_STRIPE_PRICE_BUSINESS_MONTHLY",
    "NEXT_PUBLIC_STRIPE_PRICE_BUSINESS_MONTHLY",
  ])}`,
  `EXPO_PUBLIC_REQUIRE_AUTH=${root.EXPO_PUBLIC_REQUIRE_AUTH || "false"}`,
  "",
];

fs.writeFileSync(mobileEnvPath, lines.join("\n"), "utf8");
console.log(`[sync-mobile-env] Wrote ${path.relative(repoRoot, mobileEnvPath)}`);

const required = [
  "EXPO_PUBLIC_API_BASE_URL",
  "EXPO_PUBLIC_CLERK_PUBLISHABLE_KEY",
  "EXPO_PUBLIC_SUPABASE_URL",
  "EXPO_PUBLIC_SUPABASE_ANON_KEY",
];
const mobile = parseEnvFile(mobileEnvPath);
const missing = required.filter((k) => !mobile[k]);
if (missing.length) {
  console.warn("[sync-mobile-env] Still missing (fill in .env.local):");
  for (const k of missing) console.warn(`  - ${k}`);
  process.exit(1);
}
console.log("[sync-mobile-env] All required mobile vars present.");
